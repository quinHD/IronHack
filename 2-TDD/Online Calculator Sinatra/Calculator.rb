class Calculator

	def add numA, numB
		return "The sum of #{numA} and #{numB} is #{numA + numB}"

	end

	def subtract numA, numB
		return "The substraction of #{numA} and #{numB} is #{numA - numB}"
	end

	def times numA, numB
		return "The product of #{numA} and #{numB} is #{numA * numB}"

	end

	def divide numA, numB
		return "The division of #{numA} and #{numB} is #{numA / numB}" 
	end
end